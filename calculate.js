function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function percent(a) {
    return a / 100;
}

function squareRoot(a) {
    return Math.sqrt(a);
}

function round(a) {
    return Math.round(1000 * a) / 1000;
}